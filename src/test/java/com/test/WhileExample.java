package com.test;

public class WhileExample {


        public static void main(String args[]){
            int i=10;
            while(i<15){
                System.out.println(i);
                i++;
            }

            int arr[]={2,11,45,9,7};
            //i starts with 0 as array index starts with 0 too
            int j=0;
            while(j<5){
                System.out.println(arr[j]);
                j++;
            }
        }
    }

